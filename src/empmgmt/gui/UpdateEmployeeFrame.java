    private void txtEmpNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtEmpNoActionPerformed
        txtEmpName.setText("");
        txtEmpSal.setText("");
        String empno=txtEmpNo.getText().trim();
        if(empno.isEmpty()){
           JOptionPane.showMessageDialog(null, "Please Filled employee no!","Error",JOptionPane.INFORMATION_MESSAGE);
           return;
       }
       try{
           int eno = Integer.parseInt(empno);
           Employee e=EmpDAO.findEmployeeById(eno);
           if(e==null){
               JOptionPane.showMessageDialog(null, "No record of empid "+eno+" prasent","Not Found",JOptionPane.INFORMATION_MESSAGE);
               return;
           }
           txtEmpName.setText(e.getEmpName());
           txtEmpSal.setText(String.valueOf(e.getEmpSal()));
       }
       catch(NumberFormatException ex){
           JOptionPane.showMessageDialog(null, "Please Filled numaric data!","Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
        catch(SQLException ex){
           JOptionPane.showMessageDialog(null, "DB error","Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
    }//GEN-LAST:event_txtEmpNoActionPerformed

