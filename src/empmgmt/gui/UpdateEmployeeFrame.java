    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        String empno=txtEmpNo.getText().trim();
        if(empno.isEmpty()){
           JOptionPane.showMessageDialog(null, "Please Filled employee no!","Error",JOptionPane.INFORMATION_MESSAGE);
           return;
        } 
        try{
           int eno = Integer.parseInt(empno);
           String eName=txtEmpName.getText().trim();
           String eSal=txtEmpSal.getText().trim();
           double empsal = Double.parseDouble(eSal);
           Employee e=new Employee(eno,eName,empsal);
           boolean response=EmpDAO.updateEmployee(e);
           if(response){
                JOptionPane.showMessageDialog(null, "Record Updated successfully!","Record Updated",JOptionPane.INFORMATION_MESSAGE);
           
            }
            else{
                JOptionPane.showMessageDialog(null, "Could not update the record!","Try again",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(NumberFormatException ex){
           JOptionPane.showMessageDialog(null, "Please Filled numaric data!","Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
        catch(SQLException ex){
           JOptionPane.showMessageDialog(null, "DB error","Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

